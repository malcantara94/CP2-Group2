    private void displayRecords(int empNumber) throws IOException
    {
       // List<AttendanceRecord> attendanceRecordList = AttendanceRecord.getAttendanceRecordList().stream().filter(employee -> employee.getEmployeeID()== empNumber).collect(Collectors.toList());
        List<AttendanceRecord> attendanceRecordList = AttendanceRecord.getAttendanceRecordByEmployeeId(empNumber);
        DateTimeFormatter recorDateFormat = DateTimeFormatter.ofPattern("MMM dd, yyyy");
         
        int counter = 0;

        for(AttendanceRecord attendance : attendanceRecordList.reversed())
        {
            if(counter == 4)
            {
                break;
            }

            if(empNumber == attendance.getEmployeeID())
            {
                counter+=1;

                switch(counter)
                {
                    case 1 -> {
                        jLabelDate1.setText(attendance.getRecordDate().format(recorDateFormat));
                        jTextFieldTimeIn1.setText( attendance.getTimeIn().toString());
                        jTextFieldTimeOut1.setText( attendance.getTimeOut().toString());
                    }
                    case 2 -> {
                        jLabelDate2.setText(attendance.getRecordDate().format(recorDateFormat));
                        jTextFieldTimeIn2.setText( attendance.getTimeIn().toString());
                        jTextFieldTimeOut2.setText( attendance.getTimeOut().toString());
                    }
                    case 3 -> {
                        jLabelDate3.setText(attendance.getRecordDate().format(recorDateFormat));
                        jTextFieldTimeIn3.setText( attendance.getTimeIn().toString());
                        jTextFieldTimeOut3.setText( attendance.getTimeOut().toString());
                    }
                    case 4 -> {
                        jLabelDate4.setText(attendance.getRecordDate().format(recorDateFormat));
                        jTextFieldTimeIn4.setText( attendance.getTimeIn().toString());
                        jTextFieldTimeOut4.setText( attendance.getTimeOut().toString());
                    }
                }            
            }
        }
    }
