    private void jButtonTimeOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTimeOutActionPerformed
        // TODO add your handling code here:

        try {

            EmployeeInfo employeeInfo = EmployeeInfo.getEmployeeInfoByEmployeeId(empNumber);
            //EmployeeInfo employeeInfo = EmployeeInfo.getEmployeeInfoList().stream().filter(employee -> employee.getEmployeeID()== empNumber).findAny().get();
            DateTimeFormatter dateFormat = DateTimeFormatter.ofPattern("MM/dd/yyyy");   
            DateTimeFormatter recordTimeFormat = DateTimeFormatter.ofPattern("HH:mm");

            File f = new File(filePath);
            if(f.exists()&& !f.isDirectory()) {
                List<AttendanceRecord> attendanceRecordList = AttendanceRecord.getAttendanceRecordList();

                for(AttendanceRecord attendance : attendanceRecordList )
                {
                    if(employeeInfo.getEmployeeID() == attendance.getEmployeeID() && LocalDate.now().isEqual(attendance.getRecordDate()))
                    {
                        if(!attendance.getTimeOut().toString().equals(LocalTime.MIN.toString()))
                        {
                            JOptionPane.showMessageDialog(this, "You already have timed out for today.");
                            displayRecords(employeeInfo.getEmployeeID());
                            return;
                        }
                        else
                        {

                            try
                            {
                                BufferedReader file = new BufferedReader(new FileReader(filePath));
                                StringBuilder inputBuffer = new StringBuilder();
                                String line;

                                while ((line = file.readLine()) != null) {
                                    inputBuffer.append(line);
                                    inputBuffer.append('\n');
                                }
                                file.close();

                                String inputStr = inputBuffer.toString();

                                String currentRecord = employeeInfo.getEmployeeID() + "," + employeeInfo.getEmployeeLName()+ "," + employeeInfo.getEmployeeFName()+ "," + attendance.getRecordDate().format(dateFormat) + "," + attendance.getTimeIn()+ "," + attendance.getTimeOut();
                                String newRecord = employeeInfo.getEmployeeID() + "," + employeeInfo.getEmployeeLName()+ "," + employeeInfo.getEmployeeFName()+ "," + attendance.getRecordDate().format(dateFormat) + "," + attendance.getTimeIn()+ "," + LocalTime.now().format(recordTimeFormat);

                                inputStr = inputStr.replace( currentRecord, newRecord);

                                FileOutputStream fileOut = new FileOutputStream(filePath);
                                fileOut.write(inputStr.getBytes());
                                fileOut.close();

                                displayRecords(employeeInfo.getEmployeeID());
                                
//                                Double x =LatePenalty.CalculateLatePenalty( 4, empNumber);
                                JOptionPane.showMessageDialog(this, "Successfully Timed Out");
                                return;

                            } catch (IOException e) {
                                System.out.println("Problem reading file.");
                            }
                        }
                    }
                }

            }

            JOptionPane.showMessageDialog(this, "You need to time in first.");

        }
        catch (IOException ex) {
            Logger.getLogger(MotorPHApp.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButtonTimeOutActionPerformed

